def translate(locale_code: str | None, key: str, **kwargs) -> str:
    lang = normalize_language(locale_code)
    text = _TRANSLATIONS.get(lang, {}).get(key)
    if text is None:
        text = _TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key)
    try:
        return str(text).format(**kwargs)
    except Exception:
        return str(text)
